// Sign mask table (shared with IQ3_XXS)
static const uint8_t kmask_iq2xs_local[8] = {1, 2, 4, 8, 16, 32, 64, 128};

// QK_K = 256 weights per super-block
static constexpr int QK_K = 256;
// IQ2_S block size: 2 + 64 + 8 + 8 = 82 bytes
static constexpr int IQ2_S_BLOCK_BYTES = 82;

static inline float fp16_to_fp32_iq2s(uint16_t h) {
    const uint32_t sign = (uint32_t)(h & 0x8000) << 16;
    const uint32_t exp  = (h >> 10) & 0x1F;
    uint32_t mant = h & 0x3FF;
    uint32_t result;
    if (exp == 0) {
        if (mant == 0) {
            result = sign;  // +/- zero
        } else {
            // subnormal: normalize using a signed shift count. The previous
            // unsigned `exp--` underflowed to 0xFFFFFFFF for tiny f16 scales,
            // turning them into Inf/NaN and corrupting whole quant blocks.
            int e = -1;
            do { mant <<= 1; ++e; } while (!(mant & 0x400));
            mant &= 0x3FF;
            result = sign | ((uint32_t)(127 - 15 - e) << 23) | (mant << 13);
        }
    } else if (exp == 31) {
        result = sign | 0x7F800000u | (mant << 13);  // inf / nan
    } else {
        result = sign | ((exp + 112) << 23) | (mant << 13);  // normal
    }
    float f; memcpy(&f, &result, 4); return f;
}

static inline uint16_t fp32_to_fp16_iq2s(float f) {
    uint32_t x; memcpy(&x, &f, 4);
    uint32_t sign = (x >> 16) & 0x8000;
    int32_t  exp  = ((x >> 23) & 0xFF) - 127 + 15;
    uint32_t mant = x & 0x7FFFFF;
    if (exp <= 0) {
        if (exp < -10) return (uint16_t)sign;
        mant |= 0x800000;
        uint32_t shift = (uint32_t)(1 - exp);
        mant >>= shift;
        return (uint16_t)(sign | (mant >> 13));
    } else if (exp >= 31) { return (uint16_t)(sign | 0x7C00); }
    return (uint16_t)(sign | ((uint32_t)exp << 10) | (mant >> 13));
}

ov::Tensor dequantize_iq2_s(gguf_tensor* tensor) {
    OPENVINO_ASSERT(tensor->type == GGUF_TYPE_IQ2_S,
                    "[load_gguf] dequantize_iq2_s called with wrong tensor type: ", tensor->type);

    const uint64_t num_weights = tensor->num_weights;
    OPENVINO_ASSERT(num_weights % QK_K == 0,
                    "[load_gguf] IQ2_S tensor num_weights not divisible by 256: ", num_weights);

    const uint64_t num_blocks = num_weights / QK_K;
    OPENVINO_ASSERT(tensor->bsize == num_blocks * IQ2_S_BLOCK_BYTES,
                    "[load_gguf] IQ2_S tensor size mismatch: expected ",
                    num_blocks * IQ2_S_BLOCK_BYTES, " got ", tensor->bsize);

    ov::Shape shape;
    for (int i = tensor->ndim - 1; i >= 0; i--) {
        shape.push_back(tensor->dim[i]);
    }

    ov::Tensor result(ov::element::f16, shape);
    uint16_t* out = reinterpret_cast<uint16_t*>(result.data());
    const uint8_t* raw = tensor->weights_data;

    for (uint64_t block_idx = 0; block_idx < num_blocks; ++block_idx) {
        const uint8_t* block_data = raw + block_idx * IQ2_S_BLOCK_BYTES;

        // Read f16 super-block scale
        uint16_t d_fp16;
        memcpy(&d_fp16, block_data, 2);
        const float d = fp16_to_fp32_iq2s(d_fp16);

        const uint8_t* qs     = block_data + 2;           // grid indices: 32 bytes
        const uint8_t* signs  = block_data + 2 + 32;      // sign bytes: 32 bytes
        const uint8_t* qh     = block_data + 2 + 64;      // high bits: 8 bytes
        const uint8_t* scales = block_data + 2 + 64 + 8;  // sub-block scales: 8 bytes

        for (int ib32 = 0; ib32 < QK_K / 32; ++ib32) {
            // Two 4-bit scales packed per byte
            float db[2];
            db[0] = d * (0.5f + (scales[ib32] & 0xf)) * 0.25f;
            db[1] = d * (0.5f + (scales[ib32] >> 4)) * 0.25f;

            for (int l = 0; l < 4; ++l) {
                const float dl = db[l / 2];
                // 10-bit grid index: 8 bits from qs + 2 bits from qh
                const uint16_t grid_idx = qs[4 * ib32 + l] |
                    ((uint16_t)(qh[ib32] << (8 - 2 * l)) & 0x300);
                const uint8_t* grid = reinterpret_cast<const uint8_t*>(&iq2s_grid[grid_idx]);
                const uint8_t sign_byte = signs[4 * ib32 + l];

                for (int j = 0; j < 8; ++j) {
                    float val = dl * grid[j] * ((sign_byte & kmask_iq2xs_local[j]) ? -1.f : 1.f);
                    *out++ = fp32_to_fp16_iq2s(val);
                }
            }
        }
    }
    return result;
}
